package mates;


public class OperacionesSimples {
		
	/*
	 * 		CREAMOS LOS MÉTODOS PARA REALIZAR LOS CÁLCULOS	
	 */
		
		public static Integer sumar(int n1, int n2) {
			
			int resultado;
			resultado = n1 + n2;
			return resultado;
			
		}
		
		public static Integer restar(int n1, int n2) {
				
				int resultado;
				resultado = n1 - n2;
				return resultado;
				
			}
		
		public static Integer multiplicar(int n1, int n2) {
			
			int resultado;
			resultado = n1 * n2;
			return resultado;
			
		}
		
		public static Double dividir(int n1, int n2) {
			
			double resultado;
			resultado = ((double) n1 / n2);
			return resultado;
			
		}

	}

